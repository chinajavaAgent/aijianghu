// 江湖等级称号映射
export const getLevelTitle = (level: number): string => {
  const titles: { [key: number]: string } = {
    1: '搬血境',
    2: '洞天境',
    3: '化灵境',
    4: '铭文境',
    5: '列阵境',
    6: '尊者境',
    7: '神火境',
    8: '真一境',
    9: '圣祭境',
    10: '天神境',
    11: '虚道境',
    12: '斩我境',
    13: '遁一境',
    14: '至尊境',
    15: '仙王境',
    16: '仙帝境'
  }
  return titles[level] || '修炼者'
}

// 获取下一个等级称号
export const getNextLevelTitle = (level: number): string => {
  return getLevelTitle(level + 1)
}

// 获取等级对应的颜色
export const getLevelColor = (level: number): string => {
  if (level >= 16) return 'text-red-600'     // 仙帝境 - 红色
  if (level >= 14) return 'text-purple-600'  // 至尊境/仙王境 - 紫色
  if (level >= 11) return 'text-blue-600'    // 虚道境/斩我境/遁一境 - 蓝色
  if (level >= 8) return 'text-cyan-600'     // 真一境/圣祭境/天神境 - 青色
  if (level >= 5) return 'text-green-600'    // 列阵境/尊者境/神火境 - 绿色
  return 'text-yellow-600'                   // 搬血境/洞天境/化灵境/铭文境 - 黄色
}

